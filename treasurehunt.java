    private String winner() {
        String winner = "";
        int playerName = 0;
        boolean isWinner = true;
        if (player[0].getPiratePoints() > player[1].getPiratePoints()) {
            playerName = 1;
        } else if (player[0].getPiratePoints() < player[1].getPiratePoints()) {
            playerName = 2;
        } else {
            if (player[0].getDigPoints() > player[1].getDigPoints()) {
                playerName = 1;
            } else if (player[0].getDigPoints() < player[1].getDigPoints()) {
                playerName = 2;
            } else {
                isWinner = false;
            }
        }
        if (isWinner) {
            winner = "GAME OVER... PIRATE " + playerName + " WON THE GAME WITH " + player[playerName - 1].getPiratePoints() + " PIRATE POINTS AND " + player[playerName - 1].getDigPoints() + " DIG POINTS!";
        } else {
            winner = "TIS BUT A DRAW... BOTH PLAYER HAD " + player[playerName].getPiratePoints() + " PIRATE POINTS.";
        }

        return winner;
    }

    public void dig(javax.swing.JButton button) {
        if (gameOver(player[0].getDigPoints(), player[1].getDigPoints())) {

            messageLabel.setText("Game is Over");
            turnLabel.setText(winner());

        } else if (button.getBackground().getRGB() == -16751002 && player[round % 2].getDigPoints() > 0) { //if game isn't over and position hasn't been chosen, dig
            System.out.println("Point " + button.getName() + " was dug.");
            button.setBackground(Color.black);

            for (int i = 0; i < treasures.size(); i++) {
                if (treasures.get(i).equals(button.getName())) {
                    button.setBackground(Color.yellow);
                    treasures.remove(i);
                    specials.remove(i);

                    player[round % 2].setPiratePoints(20);
                }
            }
            
            for(int i = 0; i < threats.size(); i++){
                if (threats.get(i).equals(button.getName())) {
                    button.setBackground(Color.red);
                    threats.remove(i);
                    specials.remove(i);

                    player[round % 2].setPiratePoints(-10);
                }
            }
            
            for(int i = 0; i < miniTreasures.size(); i++) {
               if (miniTreasures.get(i).equals(button.getName())) {
                    button.setBackground(Color.GREEN);
                    miniTreasures.remove(i);
                    specials.remove(i);

                    player[round % 2].setPiratePoints(5);
                }
            }

            player[round % 2].setDigPoints(-1);
            round++;
            updateLabels();

            if (gameOver(player[0].getDigPoints(), player[1].getDigPoints())) {
                updateLabels();
                turnLabel.setText(winner());
                if(treasures.size()<=0){
                    turnPanel.remove(yesButton);
                    turnPanel.remove(noButton);
                    turnPanel.remove(buyMessage);
                }
            }

        } else { //if position has already been chosen, won't dig
            messageLabel.setText("No dig points left, please buy a shovel.");
        }
    }

    private void updateLabels() {
        turnLabel.setText("PIRATE " + (1 + ((round) % 2)) + "'S TURN");
        piratepoints0.setText(String.valueOf(player[0].getPiratePoints()));
        piratepoints1.setText(String.valueOf(player[1].getPiratePoints()));
        digpoints0.setText(String.valueOf(player[0].getDigPoints()));
        digpoints1.setText(String.valueOf(player[1].getDigPoints()));
        messageLabel.setText("");
        System.out.println("Round " + (1 + (round % 2)));
    }

