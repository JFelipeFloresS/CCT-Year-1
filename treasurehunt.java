    private void noButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_noButtonActionPerformed
        if (player[round % 2].getDigPoints() <= 0) {

            turnLabel.setText("PIRATE " + ((round + 1) % 2) + "'S TURN");
            round++;
            updateLabels();

        } else {
            messageLabel.setText("You still have dig points left, please dig a location.");
        }
    }//GEN-LAST:event_noButtonActionPerformed

    private void yesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yesButtonActionPerformed
        Random rd = new Random();
        if (player[round % 2].getDigPoints() <= 0) {
            if (player[round % 2].getPiratePoints() >= 15) {
                player[round % 2].setPiratePoints(-15);
                player[round % 2].setDigPoints(4 + rd.nextInt(3));
                round++;
                updateLabels();
            } else {
                messageLabel.setText("Insufficient pirate points to buy a new shovel...");
            }
        } else {
            messageLabel.setText("You still have dig points left, please dig a spot.");
        }

    }//GEN-LAST:event_yesButtonActionPerformed

